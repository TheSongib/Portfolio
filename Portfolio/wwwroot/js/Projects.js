function webGeneralButton() {
    $("#webGeneralButton").addClass("active");
    $("#webMoreInfoButton").removeClass("active");
    $("#webVideoButton").removeClass("active");
    $("#webGeneral").css("display", "block");
    $("#webMoreinfo").css("display", "none");
    $("#webVideo").css("display", "none");
}

